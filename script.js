let currentLang = 'en';
let currentSlide = 0;

const slideViewer = document.getElementById('slide-viewer');
const progressDots = document.getElementById('progress-dots');
const langToggle = document.getElementById('lang-toggle');
const langText = document.getElementById('lang-text');
const prevBtn = document.getElementById('prev-btn');
const nextBtn = document.getElementById('next-btn');
const footerText = document.getElementById('footer-text');

function init() {
    renderSlides();
    renderDots();
    updateSlideVisibility();
    
    langToggle.addEventListener('click', toggleLanguage);
    prevBtn.addEventListener('click', () => changeSlide(-1));
    nextBtn.addEventListener('click', () => changeSlide(1));
    
    document.addEventListener('keydown', (e) => {
        if (e.key === 'ArrowRight' || e.key === ' ') changeSlide(1);
        if (e.key === 'ArrowLeft') changeSlide(-1);
    });
}

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'es' : 'en';
    langText.textContent = translations[currentLang].toggle;
    footerText.innerHTML = translations[currentLang].footer;
    renderSlides();
    updateSlideVisibility();
}

function renderSlides() {
    slideViewer.innerHTML = '';
    const slidesData = translations[currentLang].slides;
    
    slidesData.forEach((slide, index) => {
        const slideEl = document.createElement('div');
        slideEl.className = `slide ${slide.type === 'title' ? 'slide-title-page' : ''}`;
        
        let contentHtml = '';
        if (slide.type === 'title') {
            contentHtml = `
                <h1 class="slide-title animate-in">${slide.title}</h1>
                <p class="slide-subtitle animate-in" style="animation-delay: 0.2s">${slide.subtitle}</p>
                <p class="slide-description animate-in" style="animation-delay: 0.4s">${slide.description}</p>
                <p class="slide-date animate-in" style="animation-delay: 0.6s">${slide.date}</p>
            `;
        } else if (slide.type === 'grid') {
            contentHtml = `
                <h2 class="slide-title animate-in">${slide.title}</h2>
                <div class="slide-content animate-in" style="animation-delay: 0.2s">
                    <ul class="slide-list">
                        ${slide.content.map(item => `<li>${item}</li>`).join('')}
                    </ul>
                </div>
            `;
        } else if (slide.type === 'image') {
            contentHtml = `
                <h2 class="slide-title animate-in">${slide.title}</h2>
                <div class="slide-grid animate-in" style="animation-delay: 0.2s">
                    <div class="slide-image-wrapper">
                        <img src="${slide.image}" alt="${slide.title}">
                    </div>
                    <div class="slide-content">
                        <ul class="slide-list">
                            ${slide.content.map(item => `<li>${item}</li>`).join('')}
                        </ul>
                    </div>
                </div>
            `;
        }
        
        slideEl.innerHTML = contentHtml;
        slideViewer.appendChild(slideEl);
    });
}

function renderDots() {
    progressDots.innerHTML = '';
    const slidesData = translations[currentLang].slides;
    slidesData.forEach((_, index) => {
        const dot = document.createElement('div');
        dot.className = `dot ${index === currentSlide ? 'active' : ''}`;
        dot.addEventListener('click', () => goToSlide(index));
        progressDots.appendChild(dot);
    });
}

function changeSlide(direction) {
    const totalSlides = translations[currentLang].slides.length;
    currentSlide = (currentSlide + direction + totalSlides) % totalSlides;
    updateSlideVisibility();
}

function goToSlide(index) {
    currentSlide = index;
    updateSlideVisibility();
}

function updateSlideVisibility() {
    const slides = document.querySelectorAll('.slide');
    const dots = document.querySelectorAll('.dot');
    
    slides.forEach((slide, index) => {
        if (index === currentSlide) {
            slide.classList.add('active');
            slide.style.display = 'flex';
        } else {
            slide.classList.remove('active');
            slide.style.display = 'none';
        }
    });
    
    dots.forEach((dot, index) => {
        dot.classList.toggle('active', index === currentSlide);
    });
}

init();
